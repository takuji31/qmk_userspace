void pointing_device_init_user(void) {
    set_auto_mouse_layer(_MOUSE);
    if (dynamic_keymap_get_keycode(3,9,0) == 0x7701) {
        set_auto_mouse_enable(true);
    } else {
        set_auto_mouse_enable(false);
    }
}




