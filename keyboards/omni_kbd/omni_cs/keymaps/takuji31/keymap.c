const char* get_layer_name_user(uint8_t layer) {
    switch (layer) {
        case _BASE:   return "BASE";
        case _SUB:    return "SUB ";
        case _MOUSE:  return "MOUS";
        case 3:       return "----";
        case _CUSTOM: return "CUST";
        case _WLOWER: return "WLOW";
        case _WSYM:   return "WSYM";
        case _WFN:    return "W FN";
        case _WADJ:   return "WADJ";
        case _MLOWER: return "MLOW";
        case _MSYM:   return "MSYM";
        case _MFN:    return "M FN";
        case _MADJ:   return "MADJ";
        default:      return NULL;
    }
}

bool is_mouse_record_user(uint16_t keycode, keyrecord_t* record) {
    switch (keycode) {
        case KC_LCTL:
        case KC_LSFT:
        case KC_LALT:
        case KC_LGUI:
            return true;
        default:
            return false;
    }
}

bool process_detected_host_os_user(os_variant_t detected_os) {
    switch (detected_os) {
        case OS_MACOS:
        case OS_IOS:
            set_single_persistent_default_layer(_SUB);
            break;
        case OS_WINDOWS:
        case OS_LINUX:
        case OS_UNSURE:
            set_single_persistent_default_layer(_BASE);
            break;
    }
    return true;
}

